class Alphabets
{
public static void main(String[] args)
{
char ch;
System.out.println("lower case alphabets");
for(ch=97;ch<=122;ch++)
{
System.out.print(ch+"");
}
System.out.println();
System.out.println("upper case alphabets");
for(ch=65;ch<=90;ch++)
{
System.out.print(ch+"");
}
}
}